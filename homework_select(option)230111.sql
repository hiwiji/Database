-- 4. 현재 법학과 교수 중 가장 나이(PROFESSOR_SSN)가 많은 사람부터
--     이름(PROFESSOR_NAME)을 확인할 수 있는 SQL문장 작성
--    법학과의 '학과코드'(DEPARTMENT_NAME는 학과 테이블(TB_DEPARTMENT)을 조회해서 찾기

SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '법학과'
ORDER BY PROFESSOR_SSN;




