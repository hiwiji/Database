-- 3. 주소지(STUDENT_ADDRESS)가 강원도나 경기도인 학생들 중 
--    1900년대 학번(STUDENT_NO)을 가진 학생들의 이름(STUDENT_NAME)과 학번, 주소를 
--    이름의 오름차순으로 화면에 출력
--    출력헤더는 "학생이름", "학번", "거주지 주소" 출력 

SELECT STUDENT_NAME 학생이름, STUDENT_NO 학번, STUDENT_ADDRESS AS "거주지 주소"
FROM TB_STUDENT
WHERE STUDENT_ADDRESS LIKE '강원도%'
OR STUDENT_ADDRESS LIKE '경기도%'
AND STUDENT_NO NOT LIKE 'A%'
ORDER BY STUDENT_NAME;


SELECT STUDENT_NAME 학생이름, STUDENT_NO 학번, STUDENT_ADDRESS AS "거주지 주소"
FROM TB_STUDENT
WHERE STUDENT_NO LIKE '9%'
AND (STUDENT_ADDRESS LIKE '경기도%'
OR STUDENT_ADDRESS LIKE '강원도%')
ORDER BY 1;


